    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarActionPerformed
        int i = 0;
        double valorTotal = 0;   
        for (ItemNota item : itens) {
            String sql = "";
            sql = "INSERT INTO itemnota (idItem, idNotaFiscal, DescricaoProduto, Quantidade, PrecoUnitario) Values ('"
                    + item.getIdItem() + "','"
                    + Integer.parseInt(IdNotaFiscal.getText()) + "','"
                    + item.getDescricao() + "','"
                    + item.getQuantidade() + "','"
                    + item.getPrecoUnitario() +"')";
            int r = BD.runSQL(sql);
            
            valorTotal = valorTotal + item.getQuantidade() * item.getPrecoUnitario();          
            if (r == 1) {
                JOptionPane.showMessageDialog(null, "Inclusão do item " + item.getIdItem() + " realizada com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Problemas na Inclusão, verifique se "
                        + "você digitou os campos corretamente!");
            }
            TxtValorTotal.setText(String.valueOf(valorTotal));
           
        }
        String sql2 = "INSERT INTO notas (Codigo, IdCliente, precoTotal) Values ('"
                    + IdNotaFiscal.getText() + "','"
                    + TxtCliente.getText() + "','"
                    + valorTotal + "')";
            int r = BD.runSQL(sql2);
    }//GEN-LAST:event_BtnCadastrarActionPerformed

