    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        TxtIDItem.setText(String.valueOf(idItem));
        String descricao = TextDesc.getText();
        int quantidade = Integer.parseInt(txtQnt.getText());
        double preco = Double.parseDouble(TxtPreco.getText());
        double valorTotal = quantidade * preco;
        ItemNota item = new ItemNota(idItem, descricao, quantidade, preco);
        model.addRow(new Object[]{idItem, descricao, quantidade, preco, valorTotal});
        idItem++;
        itens.add(item);
        TxtIDItem.setText(String.valueOf(idItem));
    }//GEN-LAST:event_jButton1ActionPerformed

